package variables;

public class NumberRepresentation {

    public static void main(String[] args) {

        long a = 20_000_000_000L;
        double b = 435.567650;

        int c = 0b1010_1111_0101;
        System.out.println(c);

        int d = 0x12AF_2020;
        System.out.println(d);

        int e = 0444;
        System.out.println(e);
    }
}
