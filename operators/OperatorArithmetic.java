package operators;

public class OperatorArithmetic {

    public static void main(String[] args) {

        int a = 10;
        int b = 2;
        int c = 5;
        int d = a * b - c;
        System.out.println(d);

        int e = 4;
        int f = 2;
        int g = e * (f / e);
        System.out.println(g);
    }
}
